#include <stddef.h>

void	ft_bzero(void *ptr, const size_t len)
{
	__uint128_t *ptr_128;
	size_t		len_128;

	ptr_128 = (__uint128_t *)ptr;
	len_128 = len * 0.0625;
	while (len_128--)
		ptr_128[len_128] = 0;
}
